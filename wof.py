class WOFPlayer:

    prizeMoney = 0
    prizes = []

    def __init__(self, name):
        self.name = name

    def addMoney(amt):
        self.prizeMoney = self.prizeMoney + amt

    def goBankrupt():
        self.prizeMoney = 0

    def addPrize(prize):
        self.prizes.append(prize)

    def __str__():
        return "{} (${})".format(self.name, self.prizeMoney)
